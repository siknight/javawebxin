/*
   编程实现布尔类型的使用，也就是boolean类型
*/
public class TestBoolean{

    public static void main(String[] args){

        boolean b1 = true;
        System.out.println("b1 = " + b1); //b1 = true

        System.out.println("-----------------------");
        b1 = false;
        System.out.println("b1 = " + b1); // b1 = false

        System.out.println("------------------------");
        //b1 = 1; error 不兼容的类型
        //b1 = 0; error 
    }
}